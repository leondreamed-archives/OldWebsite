<p>I came across the idea of the "Impossible List" while exploring <a href="https://www.collegeinfogeek.com/">College Info Geek</a>. As I scrolled through Thomas Frank's mighty list of goals and accomplishments, I decided that I wanted to make a similar list of my own.</p>
<p>Before I go on, I do want to note that the original creator of this amazing idea, <a href="https://www.impossiblehq.com/">Joel Runyon</a> also has <a href="https://impossiblehq.com/impossible-list/">an amazing list of his own</a> that you definitely shouldn't dismiss either.</p>
<p>If you've never heard of the Impossible List before, it's a list of tasks one wishes to accomplish. It may sound similar to a bucket list, but there's one crucial difference.</p>
<p><strong>A bucket list lacks a present desire to get the tasks done.</strong></p>
<p>Of course, we all want to do everything on our bucket list. However, we don't really have a plan or date to tackle those goals. There's no real incentive to get those wishes fulfilled. An impossible list, however, contains ideas that we wish to pursue and <strong>grow.</strong> A bucket list has the mentality of "I'll get to those things later in life" compared to the more immediate mentality of the impossible list: "Taking action as soon as I can."</p>
<p>Another major part of the impossible list is the fact that it evolves. As Joel Runyon puts it, "But I’m not done yet – every time I find a new edge of what’s possible, I keep pushing and find the next level." The impossible list is a set of tasks that grows with each completion. Unlike a checklist, these incremental goals grow every time you pass the original increment. This provides high Expectancy leading to a higher motivation for a want to pursue those goals in the immediate present.</p>
<p>The impossible list is about pushing your limits. Anybody could fill a list of tiny, easy goals that meant little to them when accomplished. The impossible list is meant to be filled with challenges that constantly push you to the next level.</p>
<p>I would recommend everybody to create their own type of impossible list. Nobody should be happy with settling for the 'okay' or 'comfortable', that's what we look back with regret on later in life. You've got this one chance and you should use it to the best of your abilities.</p>

